## Activity 2: How to Make Toast
import time 

slices = input("How many slices of bread do you want to toast? ")
level = input("Set the toaster to level (1-5): ")
print(f"Get {slices} slices of breaad.")
time.sleep(1)
print(f"Put the bread in the toaster.")
time.sleep(1)
print(f"Set the toaster to level {level}.")
time.sleep(1)
print(f"Wait for the toast to be ready...\n\n\n\n\n")
time.sleep(5)
print(f"Take the toast out.")
time.sleep(1)
topping =input("What topping would you like on your toast? ")
print(f"Add {topping} to the toast")

